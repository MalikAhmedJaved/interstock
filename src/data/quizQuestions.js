// Function to get questions for a specific quiz
export const getQuestionsForQuiz = (quizId, totalQuestions) => {
  let questionSet = []
  
  // Select question set based on quiz ID
  if (quizId === 5) {
    // Technical Analysis Quiz
    questionSet = technicalAnalysisQuestions
  } else if (quizId === 6) {
    // Portfolio Management Quiz
    questionSet = portfolioManagementQuestions
  } else {
    // Use general question bank for other quizzes
    questionSet = generalQuestionBank
  }
  
  // Return the requested number of questions
  // For quizzes with specific questions, return all of them
  // For general quizzes, return first N questions
  if (quizId === 5 || quizId === 6) {
    return questionSet.slice(0, Math.min(totalQuestions, questionSet.length))
  } else {
    return questionSet.slice(0, Math.min(totalQuestions, questionSet.length))
  }
}
